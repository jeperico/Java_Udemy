package fundamentos;

public class Inferencia {

	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		var b = 4.5;
		System.out.println(b);
		
		var c = "Text";
		System.out.println(c);
		c = "Other Text";
		System.out.println(c);
		
		double d;
		d = 123.45;
		System.out.println(d);
		
		// var e;
		// e = 123.45;
	}
}
